let num = 0;
console.log(num++);
console.log(++num);
console.log(num);
 

//0
//2
//2
//Explanation:
// The ++ operator increments the value of the variable by 1.
// The post-increment operator (num++) returns the current value of num and then increments it by 1.
// In the first console.log, num is 0 and then it is incremented to 1.
// The pre-increment operator (++num) increments the value of num by 1 and then returns the value.
// In the second console.log, num is incremented to 2 and then returned.
// In the third console.log, the value of num is 2.
